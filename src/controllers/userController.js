export const getEdit = (req, res) => {
  return res.render("edit-profile", { pageTitle: "Edit Profile" });
};

export const postEdit = async (req, res) => {
  const {
    session: {
      user: { _id, avatarUrl },
    },
    body: { name, email, username, location },
    file,
  } = req;
  /*유저가 프로필을 수정할 때 데이터베이스에서 체크 진행.
User에서 이메일 또는 유저네임이 입력값과 일치한 사용자를 검색. 단, 검색 결과에서 현재 사용자 ID와 다른 사용자만 포함.
$ne는 not equal의 약어로 주어진 값과 필드의 값을 비교하는 MongoDB 연산자*/
  const existingUser = await User.findOne({
    $or: [{ email }, { username }],
    _id: { $ne: _id },
  });

  if (existingUser) {
    const errorMessage = [];
    if (existingUser.email === email) {
      errorMessage.push("This email is already taken.");
    }
    if (existingUser.username === username) {
      errorMessage.push("This username is already taken.");
    }
    return res.status(400).render("edit-profile", {
      pageTitle: "Edit Profile",
      errorMessage: errorMessage.join(" "),
    });
  } else {
    // 중복되지 않으면 유저 정보 업데이트 실행
    const updatedUser = await User.findByIdAndUpdate(
      _id,
      {
        avatarUrl: file ? file.path : avatarUrl,
        name,
        email,
        username,
        location,
      },
      { new: true }
    );
    req.session.user = updatedUser;
    res.redirect("/users/edit");
  }
};

export const getChangePassword = (req, res) => {
  if (req.session.user.socialOnly === true) {
    return res.redirect("/");
  }
  return res.render("users/change-password", { pageTitle: "Change Password" });
};
export const postChangePassword = async (req, res) => {
  const {
    session: {
      user: { _id },
    },
    body: { oldPassword, newPassword, newPasswordConfirmation },
  } = req;
  const user = await User.findById(_id);
  const ok = await bcrypt.compare(oldPassword, user.password);
  if (!ok) {
    return res.status(400).render("users/change-password", {
      pageTitle: "Change Password",
      errorMessage: "The current password is incorrect",
    });
  }
  if (newPassword !== newPasswordConfirmation) {
    return res.status(400).render("users/change-password", {
      pageTitle: "Change Password",
      errorMessage: "The password does not match the confirmation",
    });
  }
  if (oldPassword === newPassword) {
    return res.status(400).render("users/change-password", {
      pageTitle: "Change Password",
      errorMessage: "The old password equals new password",
    });
  }
  user.password = newPassword;
  await user.save();
  return res.redirect("/users/logout");
};

export const see = async (req, res) => {
  const { id } = req.params;
  const user = await User.findById(id).populate({
    // 동영상을 최신순으로 정렬
    path: "videos",
    options: { sort: { createdAt: "desc" } },
  });

  if (!user) {
    return res.status(404).render("404", { pageTitle: "User not found" });
  }
  return res.render("users/profile", {
    pageTitle: user.name,
    user,
  });
};
